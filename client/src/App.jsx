import React from "react";
import Login from "./pages/Login";
import { ToastContainer } from "react-toastify";
import "react-toastify/dist/ReactToastify.css";
import Hero from "./pages/student/Hero";
import { createBrowserRouter, RouterProvider } from "react-router-dom";
import MainLayout from "./layout/MainLayout";
import Courses from "./pages/student/Courses";
import MyLearning from "./pages/student/MyLearning";
import Profile from "./pages/student/Profile";
import Sidebar from "./pages/admin/Sidebar";
import Dashboard from "./pages/admin/Dashboard";
import CourseTable from "./pages/admin/course/CourseTable";
import AddCourse from "./pages/admin/course/AddCourse";
import EditCourse from "./pages/admin/course/EditCourse";
import CreateLecture from "./pages/admin/lecture/CreateLecture";
import EditLecture from "./pages/admin/lecture/EditLecture";
import CourseDetail from "./pages/student/CourseDetail";
import CourseProgress from "./pages/student/CourseProgress";
import SearchPage from "./pages/student/SearchPage";
import {
  AdminRoute,
  AuthenticatedUser,
  CompanyRoute,
  ProtectedRoute,
} from "./components/UI/ProtectedRoutes";
import PurchaseCourseProtectedRoute from "./components/UI/PurchaseCourseProtectedRoute";
import ResetPassword from "./pages/student/ResetPassword";
import AdminRequest from "./pages/student/AdminRequest";
import CompanySidebar from "./pages/company/CompanySidebar";
import GetAllUsers from "./pages/company/GetAllUsers";
import GetAllInstructors from "./pages/company/GetAllInstructors";
import GetAllCourses from "./pages/company/GetAllCourses";
import InstructorCourses from "./pages/company/InstructorCourses";
import GetAllAdminRequest from "./pages/company/GetAllAdminRequest";

const appRouter = createBrowserRouter([
  {
    path: "/",
    element: <MainLayout />,
    children: [
      {
        path: "/",
        element: (
          <>
            <Hero />
            <Courses />
          </>
        ),
      },
      {
        path: "login",
        element: (
          <AuthenticatedUser>
            <Login />
          </AuthenticatedUser>
        ),
      },
      {
        path: "my-learning",
        element: (
          <ProtectedRoute>
            <MyLearning />
          </ProtectedRoute>
        ),
      },
      {
        path: "profile",
        element: (
          <ProtectedRoute>
            <Profile />
          </ProtectedRoute>
        ),
      },
      {
        path: "course/search",
        element: (
          <ProtectedRoute>
            <SearchPage />
          </ProtectedRoute>
        ),
      },
      {
        path: "course-detail/:courseId",
        element: (
          <ProtectedRoute>
            <CourseDetail />
          </ProtectedRoute>
        ),
      },
      {
        path: "admin-request",
        element: (
          <ProtectedRoute>
            <AdminRequest />
          </ProtectedRoute>
        ),
      },
      {
        path: "course-progress/:courseId",
        element: (
          <ProtectedRoute>
            <PurchaseCourseProtectedRoute>
              <CourseProgress />
            </PurchaseCourseProtectedRoute>
          </ProtectedRoute>
        ),
      },
      {
        path: "/reset-password",
        element: <ResetPassword />,
      },
      // admin routes
      {
        path: "admin",
        element: (
          <AdminRoute>
            <Sidebar />
          </AdminRoute>
        ),
        children: [
          {
            path: "dashboard",
            element: <Dashboard />,
          },
          {
            path: "course",
            element: <CourseTable />,
          },
          {
            path: "course/create",
            element: <AddCourse />,
          },
          {
            path: "course/:courseId",
            element: <EditCourse />,
          },
          {
            path: "course/:courseId/lecture",
            element: <CreateLecture />,
          },
          {
            path: "course/:courseId/lecture/:lectureId",
            element: <EditLecture />,
          },
        ],
      },
      // company routes
      {
        path: "company",
        element: (
          <CompanyRoute>
            <CompanySidebar />
          </CompanyRoute>
        ),
        children: [
          {
            path: "users",
            element: <GetAllUsers />,
          },
          {
            path: "instructors",
            element: <GetAllInstructors />,
          },
          {
            path: "instructors/courses",
            element: <GetAllCourses />,
          },
          {
            path: "instructor/:instructorId/courses",
            element: <InstructorCourses />,
          },
          {
            path: "admin-request",
            element: <GetAllAdminRequest />,
          },
        ],
      },
    ],
  },
]);

const App = () => {
  return (
    <main className="bg-[#EEFBF3]">
      <ToastContainer
        position="top-center"
        autoClose={1000}
        newestOnTop={true}
        closeButton={true}
        pauseOnFocusLoss={false}
      />
      <RouterProvider router={appRouter} />
    </main>
  );
};

export default App;
